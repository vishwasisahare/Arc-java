package ObjectPractice;


public class loops {
     public static void main(String[] args) {
         
        int i= 0;           //for loop
      for(i =0 ; i <= 10 ; i++){
          System.out.println(i);
      }
      
      
       int m = 0;            //while loop
         while (m <= 50) {             
              System.out.println(m);
              m++;
         }
         
        int n = 0;             //do while
         do {             
             System.out.println(n);
             n++;
         } while (n < 30);
      
         
    }
}
